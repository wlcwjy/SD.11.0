uint16_t slow_ease_out_circ(uint16_t start, uint16_t end, uint16_t value) 
{
	uint32_t t1;
	uint32_t t2;
	
	if(start<end)
	{
		return end;
	}
	
	if(start<value)
	{
		return end;
	}
	
	t1=start*start;
	t2=(start-value);
	t2=t2*t2;
	t1=t1-t2;
	t1=sqrt(t1);
	return t1;
}

