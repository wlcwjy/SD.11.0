uint16_t slow_ease_in_out_sine_U0_180(uint16_t start, uint16_t end, uint16_t value) 
{
	uint16_t delta;
	uint32_t t;
	
	if (value<=start)
    {
        return start;
    }

    if (value>=end)
    {
        return value;
    }

    if(start>end)
    {
      return start;
    }
    
    delta=end-start;
	
	if(value<=(delta>>1))
	{
		//-(Math.cos(Math.PI * x) - 1) / 2;(0~PI/2)
		//return ((sin_cos_table[0xFFF]+sin_cos_table[0xFFF - ((uint32_t)(0xFFF*value)/(delta/2))])/2)*delta/sin_cos_table[0xFFF];
		t=sin_cos_table[(0xFFF - ((uint32_t)(0xFFF*value)/(delta/2)))];
		t=sin_cos_table[0xFFF]-t;
		t=t/2;
		t=t*delta;
		t=t/sin_cos_table[0xFFF];
		return t;
	}
	else
	{
		//-(Math.cos(Math.PI * x) - 1) / 2;(PI/2~PI)
		//return((sin_cos_table[0xFFF]-sin_cos_table[((uint32_t)(0xFFF*value)/(delta/2))])/2)*delta/sin_cos_table[0xFFF];
		t=((uint32_t)(0xFFF*(value-delta/2))/(delta/2));
		t=sin_cos_table[t];
		t=sin_cos_table[0xFFF]+t;
		t=t/2;
		t=t*delta;
		t=t/sin_cos_table[0xFFF];
		return t;
	}
}

