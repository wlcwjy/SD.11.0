uint16_t slow_ease_in_sine_U0_90(uint16_t start, uint16_t end, uint16_t value) 
{
    uint16_t delta;

    if (value<=start)
    {
        return start;
    }

    if (value>=end)
    {
        return end;
    }

    if(start>end)
    {
      return start;
    }
    
    delta=end-start;
	
    //1 - Math.cos((x * Math.PI) / 2);(0~PI/2)
    return (sin_cos_table[0xFFF]-sin_cos_table[(0xFFF - (uint32_t)(0xFFF*value)/delta)])*delta/sin_cos_table[0xFFF]+start;
}

